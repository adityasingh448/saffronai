@dataclass
class PageScript:
    page: int
    narration: str
    focus: str
    highlight_terms: list[str]


@dataclass
class WalkthroughScript:
    title: str
    full_script: str
    page_scripts: list[PageScript]
    source: str

    def to_dict(self) -> dict[str, Any]:
        data = asdict(self)
        return data


def write_walkthrough_script(
    pages: list[PdfPage],
    prospect_name: str,
    target_company: str,
    language: str,
    max_minutes: int,
) -> WalkthroughScript:
    language = "English"
    if settings.openai_api_key:
        try:
            return _write_with_openai(pages, prospect_name, target_company, language, max_minutes)
        except Exception as exc:
            print(f"OpenAI script generation failed, falling back locally: {exc}")

    return _write_locally(pages, prospect_name, target_company, language, max_minutes)


def _write_with_openai(
    pages: list[PdfPage],
    prospect_name: str,
    target_company: str,
    language: str,
    max_minutes: int,
) -> WalkthroughScript:
    target_words = _target_words(max_minutes)
    page_targets = _page_word_targets(pages, target_words)
    page_payload = [
        {
            "page": page.page_number,
            "heading": page.heading,
            "target_words": page_targets[index],
            "lines": page.lines[:140],
            "full_text_excerpt": page_excerpt(page.text, limit=5200),
        }
        for index, page in enumerate(pages)
    ]

    system_prompt = (
        "You are a senior growth consultant creating a direct PDF walkthrough voiceover "
        "for the company receiving this report. The narration must sound calm, emotionally natural, "
        "warm, clear, human, persuasive, and confident. Speak directly to the viewer using you, your team, "
        "and your business. Use only the PDF text supplied by the user. Do not invent metrics, problems, "
        "recommendations, or facts."
    )
    user_prompt = {
        "prospect_name": prospect_name or "the prospect",
        "target_company": target_company or "the target company",
        "language": language,
        "target_minutes": max_minutes,
        "target_total_words": target_words,
        "pages": page_payload,
        "writing_rules": [
            "Explain the page as numbered business pointers: Point 1, Point 2, and so on.",
            "When a page heading is supplied, use that PDF heading as the page focus and as the first pointer.",
            "Each pointer must come from a distinct line, metric, finding, recommendation, or heading in that page's PDF text.",
            "For each pointer, say what the PDF says, what it means, why it matters, and what can be improved, but only if that is supported by the PDF text.",
            "Address the viewer directly using 'you', 'your team', and 'your business'.",
            "Never use the word formed by c-l-i-e-n-t in narration, titles, focus labels, or on-screen text.",
            "Do not write speaker notes or internal instructions. The copy goes directly to the viewer.",
            "Do not mention outreach, pitching, prospecting, or sales conversation.",
            "Do not repeatedly say visual, visuals, or visual section.",
            "Do not pad the narration. If a page has limited text, keep that page concise instead of repeating the same idea.",
            "Avoid generic filler phrases like 'this is not just an observation' or 'the recommendation is simple'.",
            "Use warm consultative phrasing, natural pauses, varied sentence length, and light emotional emphasis. Add occasional ellipses for thoughtful pauses, but do not overuse them.",
            "Never say this was generated by AI. Never sound like a script reader.",
            "Write only in clear, natural English.",
        ],
        "output_contract": {
            "title": "short video title",
            "page_scripts": [
                {
                    "page": "page number",
                    "narration": "spoken narration close to that page's target_words, structured around numbered pointers",
                    "focus": "short on-screen focus label; prefer the supplied PDF heading",
                    "highlight_terms": "3-5 concise pointer labels copied from this page's PDF text; these are used for camera movement labels only, not visible highlight boxes",
                }
            ],
        },
    }

    response = requests.post(
        "https://api.openai.com/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {settings.openai_api_key}",
            "Content-Type": "application/json",
        },
        json={
            "model": settings.openai_model,
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": json.dumps(user_prompt, ensure_ascii=False)},
            ],
            "temperature": 0.45,
        },
        timeout=90,
    )
    response.raise_for_status()
    content = response.json()["choices"][0]["message"]["content"]
    payload = _parse_json_response(content)

    page_scripts = []
    for index, item in enumerate(payload.get("page_scripts", [])):
        page = pages[min(index, len(pages) - 1)]
        focus = str(item.get("focus") or page.heading or f"Page {index + 1}").strip()
        highlight_terms = [str(term).strip() for term in item.get("highlight_terms", []) if str(term).strip()]
        highlight_terms = _dedupe_terms(([page.heading] if page.heading else []) + highlight_terms)
        page_scripts.append(
            PageScript(
                page=int(item.get("page", index + 1)),
                narration=str(item.get("narration", "")).strip(),
                focus=focus,
                highlight_terms=highlight_terms[:6],
            )
        )

    if not page_scripts:
        raise ValueError("OpenAI returned no page scripts.")

    page_scripts = _ensure_target_length(page_scripts, pages, prospect_name, target_company, language, target_words)
    full_script = "\n\n".join(item.narration for item in page_scripts)
    title = str(payload.get("title") or _default_title(target_company)).strip()
    return WalkthroughScript(title=title, full_script=full_script, page_scripts=page_scripts, source="openai")


def _parse_json_response(content: str) -> dict[str, Any]:
    content = content.strip()
    fence = re.search(r"```(?:json)?\s*(.*?)```", content, flags=re.DOTALL | re.IGNORECASE)
    if fence:
        content = fence.group(1).strip()
    return json.loads(content)


def _write_locally(
    pages: list[PdfPage],
    prospect_name: str,
    target_company: str,
    language: str,
    max_minutes: int,
) -> WalkthroughScript:
    company = target_company.strip() or "your business"
    company_for_sentence = company.rstrip(".")
    prospect = prospect_name.strip() or "there"
    is_hinglish = language.lower() in {"hinglish", "hindi", "hi"}
    target_words = _target_words(max_minutes)
    page_targets = _page_word_targets(pages, target_words)
    scripts: list[PageScript] = []
    point_counter = 1

    for index, page in enumerate(pages):
        focus = clean_script_text(page.heading or _focus_from_text(page.text, page.page_number))
        points = _extract_points(page)
        target_for_page = page_targets[index]
        selected_points = _points_for_target(_dedupe_focus_points(points, focus), target_for_page)

        if is_hinglish:
            narration = _build_hinglish_detail(selected_points, target_for_page, focus, point_counter)
        else:
            narration = _build_english_detail(selected_points, target_for_page, focus, point_counter)
        point_counter += max(1, len(selected_points))

        scripts.append(
            PageScript(
                page=page.page_number,
                narration=narration,
                focus=focus,
                highlight_terms=selected_points,
            )
        )

    intro = (
        f"Hi {prospect}, this walkthrough covers the key findings for {company_for_sentence} and the improvements to prioritize next."
        if not is_hinglish
        else f"Hi {prospect}, ye walkthrough {company_for_sentence} ke key findings aur next improvements simple way mein explain karta hai."
    )
    closing = (
        "Next, turn these observations into a focused action plan and measure progress week by week."
        if not is_hinglish
        else "Ab in observations ko focused action plan mein convert karein aur week by week progress measure karein."
    )

    if scripts:
        scripts[0].narration = f"{intro} {scripts[0].narration}"
        scripts[-1].narration = f"{scripts[-1].narration} {closing}"

    full_script = "\n\n".join(item.narration for item in scripts)
    scripts = _ensure_target_length(scripts, pages, prospect_name, target_company, language, target_words)
    full_script = "\n\n".join(item.narration for item in scripts)
    return WalkthroughScript(
        title=_default_title(company),
        full_script=full_script,
        page_scripts=scripts,
        source="local",
    )


def _default_title(company: str) -> str:
    company = company.strip() or "Prospect"
    return f"{company} report walkthrough"


def _focus_from_text(text: str, page_number: int) -> str:
    text = text.strip()
    if not text:
        return f"report section on page {page_number}"

    candidates = re.split(r"(?<=[.!?])\s+|\n+", text)
    candidates = [item.strip(" :-|") for item in candidates if len(item.strip()) >= 8]
    first = candidates[0] if candidates else text[:90]
    words = first.split()
    return " ".join(words[:10])


def _highlight_terms_for_page(page: PdfPage) -> list[str]:
    return []


def _dedupe_terms(terms: list[str]) -> list[str]:
    deduped: list[str] = []
    seen: set[str] = set()
    for term in terms:
        cleaned = " ".join((term or "").split())
        if not cleaned:
            continue
        key = re.sub(r"[^a-z0-9]+", "", cleaned.lower())
        if key in seen:
            continue
        seen.add(key)
        deduped.append(cleaned)
    return deduped


def _dedupe_focus_points(points: list[str], focus: str) -> list[str]:
    cleaned_focus = _point_key(focus)
    result: list[str] = []
    seen: set[str] = set()
    for point in points:
        key = _point_key(point)
        if not key or key in seen:
            continue
        seen.add(key)
        if cleaned_focus and key == cleaned_focus:
            if not result:
                result.append(point)
            continue
        result.append(point)
    return result or points


def _summarize_text(text: str) -> str:
    text = re.sub(r"\s+", " ", text or "").strip()
    if not text:
        return "the report gives a short finding that should be reviewed before choosing the next improvement."

    sentences = re.split(r"(?<=[.!?])\s+", text)
    useful = [sentence.strip() for sentence in sentences if len(sentence.strip()) > 20]
    if not useful:
        useful = [text]

    summary = " ".join(useful[:2])
    words = summary.split()
    if len(words) > 55:
        summary = " ".join(words[:55]) + "..."
    return summary


def _target_words(max_minutes: int) -> int:
    minutes = max(1, min(int(max_minutes or 1), 6))
    return minutes * 95


def _page_word_targets(pages: list[PdfPage], total_words: int) -> list[int]:
    weights = [max(len(page.text.split()), 80) for page in pages]
    total_weight = sum(weights) or len(pages)
    targets = [max(45, round(total_words * weight / total_weight)) for weight in weights]
    difference = total_words - sum(targets)
    if targets:
        targets[-1] += difference
    return targets


