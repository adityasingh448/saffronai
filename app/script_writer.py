LOW_VALUE_POINT_PATTERNS = (
    r"^page\s*\d+(\s*/\s*\d+)?$",
    r"^saffron\s*ai$",
    r"^saffronedge$",
    r"^all rights reserved$",
    r"^confidential$",
    r"^table of contents$",
    r"^thank you\.?$",
)


def _extract_points(page: PdfPage) -> list[str]:
    scored: list[tuple[float, int, str]] = []

    if page.heading:
        heading = _clean_point(page.heading)
        if heading:
            scored.append((90.0, -1, heading))

    for position, line in enumerate(page.lines):
        cleaned = _clean_point(line)
        if cleaned:
            score = _point_score(cleaned, position, page.heading)
            if score > 0:
                scored.append((score, position, cleaned))

    if len(scored) < 4:
        sentences = re.split(r"(?<=[.!?])\s+", page.text)
        for position, sentence in enumerate(sentences, start=200):
            cleaned = _clean_point(sentence)
            if cleaned:
                score = _point_score(cleaned, position, page.heading)
                if score > 0:
                    scored.append((score, position, cleaned))

    selected: list[tuple[float, int, str]] = []
    seen: set[str] = set()
    for score, position, candidate in sorted(scored, key=lambda item: (-item[0], item[1])):
        key = _point_key(candidate)
        if key in seen:
            continue
        seen.add(key)
        selected.append((score, position, candidate))
        if len(selected) >= 8:
            break

    selected.sort(key=lambda item: item[1])
    return [candidate for _, _, candidate in selected] or [_summarize_text(page.text)]


def _clean_point(text: str) -> str:
    text = clean_script_text(text)
    text = re.sub(r"^[\-•*–—\d.)\s]+", "", text).strip()
    text = text.strip("\"'` ")
    if not text or _is_low_value_point(text):
        return ""
    if len(text.split()) > 38:
        text = " ".join(text.split()[:38]) + "..."
    return text


def _is_low_value_point(text: str) -> bool:
    cleaned = clean_script_text(text).strip(" :-|")
    if len(cleaned) < 8:
        return True

    lower = cleaned.lower()
    if re.search(r"https?://|www\.|@", lower):
        return True
    if re.fullmatch(r"[\W\d_]+", cleaned):
        return True
    if len(re.findall(r"[A-Za-z]", cleaned)) < 5:
        return True
    if sum(1 for char in cleaned if char.isdigit()) > max(4, len(cleaned) * 0.45):
        return True

    return any(re.fullmatch(pattern, lower) for pattern in LOW_VALUE_POINT_PATTERNS)


def _point_key(text: str) -> str:
    words = re.findall(r"[a-z0-9]+", text.lower())
    return " ".join(words[:18])


def _point_score(text: str, position: int, heading: str) -> float:
    if _is_low_value_point(text):
        return 0.0

    words = text.split()
    lower = text.lower()
    score = 18.0

    if 4 <= len(words) <= 18:
        score += 16.0
    elif len(words) <= 3:
        score -= 8.0
    elif len(words) > 30:
        score -= 5.0

    if re.search(r"\d|%|\$|rs\.?|inr|usd|x\b|:", lower):
        score += 12.0

    normalized_terms = set(re.findall(r"[a-z0-9]+", lower))
    score += sum(5.0 for term in IMPORTANT_POINT_TERMS if term in normalized_terms)

    if heading and _point_key(text) == _point_key(heading):
        score += 22.0

    if position < 12:
        score += max(0.0, 10.0 - position * 0.7)

    if lower.endswith("?"):
        score -= 6.0

    return score


def clean_script_text(text: str) -> str:
    replacements = {
        "â€”": "-",
        "â€“": "-",
        "â†’": " to ",
        "—": "-",
        "–": "-",
        "→": " to ",
        "←": " from ",
        "“": '"',
        "”": '"',
        "’": "'",
        "‘": "'",
        "â€œ": '"',
        "â€": '"',
        "â€™": "'",
        "â€˜": "'",
        "â€¢": "",
        "â€¦": "...",
        "Â": "",
    }
    cleaned = text or ""
    for bad, good in replacements.items():
        cleaned = cleaned.replace(bad, good)
    cleaned = _normalize_report_terms(cleaned)
    phrase_replacements = {
        "Applied Al": "Applied AI",
        "eitherkeepasavisualherosub-headline": "either keep it as a visual hero sub-headline",
        "andaddavisuallysmaller": "and add a visually smaller",
        "butcrawlable": "but crawlable",
        "orrestructuresothe": "or restructure so the",
        "includesthe": "includes the",
        "consultingkeyword": "consulting keyword",
    }
    for bad, good in phrase_replacements.items():
        cleaned = cleaned.replace(bad, good)
    cleaned = re.sub(r"(?<=[a-z])(?=[A-Z])", " ", cleaned)
    cleaned = re.sub(r"(?<=[A-Za-z])(?=\d)", " ", cleaned)
    cleaned = re.sub(r"(?<=\d)(?=[A-Za-z])", " ", cleaned)
    cleaned = re.sub(r"\s+", " ", cleaned)
    return cleaned.strip()


def _normalize_report_terms(text: str) -> str:
    normalized = text or ""
    normalized = re.sub(r"\bOn\s*[-–—]?\s*[1lI]\s+Page\s+SEO\b", "On-Page SEO", normalized, flags=re.IGNORECASE)
    normalized = re.sub(r"\bOn\s+Page\s+SEO\b", "On-Page SEO", normalized, flags=re.IGNORECASE)
    normalized = re.sub(r"\bOn\s*[-–—]\s*Page\s+SEO\b", "On-Page SEO", normalized, flags=re.IGNORECASE)
    normalized = re.sub(r"\bOff\s*[-–—]?\s*Page\s+SEO\b", "Off-Page SEO", normalized, flags=re.IGNORECASE)
    normalized = re.sub(r"\bog\s*image\s*alt\s*attribute\b", "OG image alt attribute", normalized, flags=re.IGNORECASE)
    normalized = re.sub(r"\bog\s*description\b", "OG description", normalized, flags=re.IGNORECASE)
    return normalized


def _spoken_text(text: str) -> str:
    spoken = clean_script_text(text)
    spoken = re.sub(r"^\s*\d+\s*[.)]\s*", "", spoken)
    spoken = re.sub(r"\b(\d{1,3})\s*/\s*100\b", r"\1 out of 100", spoken)
    spoken = re.sub(r"\$\s*([0-9]+(?:\.[0-9]+)?)\s*B\b", r"\1 billion dollars", spoken, flags=re.IGNORECASE)
    spoken = re.sub(r"\bJSON\s*[- ]\s*LD\b", "JSON linked data", spoken, flags=re.IGNORECASE)
    spoken = re.sub(r"\bH\s*1\b", "H one heading", spoken, flags=re.IGNORECASE)
    spoken = re.sub(r"\bSEO\b", "Search Engine Optimization", spoken)
    spoken = re.sub(r"\bAEO\b", "Answer Engine Optimization", spoken)
    spoken = re.sub(r"\bGEO\b", "Generative Engine Optimization", spoken)
    spoken = re.sub(r"\bSERP\b", "Search Engine Results Page", spoken)
    spoken = re.sub(r"\bCTR\b", "click-through rate", spoken)
    spoken = re.sub(r"\bCPC\b", "cost per click", spoken)
    spoken = re.sub(r"\bOG\b", "Open Graph", spoken)
    spoken = re.sub(r"\bHQ\b", "headquarters", spoken)
    spoken = re.sub(r"\bCEO\b", "chief executive officer", spoken)
    spoken = re.sub(r"\bAI\b", "artificial intelligence", spoken)
    spoken = re.sub(r"\s+", " ", spoken)
    return spoken.strip()


def _same_point(point: str, focus: str) -> bool:
    point_key = _point_key(point)
    focus_key = _point_key(focus)
    if not point_key or not focus_key:
        return False
    return point_key == focus_key or point_key in focus_key or focus_key in point_key


def _build_hinglish_detail(points: list[str], target_words: int, focus: str = "", start_index: int = 1) -> str:
    narration_parts: list[str] = []
    if not points:
        points = ["Is page mein ek short finding hai jise next improvement decide karne se pehle review karna chahiye."]

    selected = _points_for_target(points, target_words)
    for index, point in enumerate(selected, start=1):
        spoken_point = _spoken_text(point)
        ordinal = start_index + index - 1
        transition = _hinglish_point_transition(ordinal)
        if index == 1 and _same_point(point, focus):
            if len(selected) > 1:
                narration_parts.append(f"{transition}: {_spoken_text(focus)}. Isko anchor samjhein; agla signal priority clear karega.")
                continue
            narration_parts.append(
                f"{transition}: {_spoken_text(focus)}. {_hinglish_focus_bridge(point)} {_hinglish_meaning(point)} {_hinglish_action(point)}"
            )
            continue
        narration_parts.append(
            f"{transition}: {spoken_point}. "
            f"{_hinglish_meaning(point)} "
            f"{_hinglish_action(point)}"
        )

    return clean_script_text(" ".join(narration_parts))


def _build_english_detail(points: list[str], target_words: int, focus: str = "", start_index: int = 1) -> str:
    narration_parts: list[str] = []
    if not points:
        points = ["The report gives a short finding that should be reviewed before choosing the next improvement."]

    selected = _points_for_target(points, target_words)
    for index, point in enumerate(selected, start=1):
        ordinal = start_index + index - 1
        transition = _english_point_transition(ordinal)
        if index == 1 and _same_point(point, focus):
            if len(selected) > 1:
                narration_parts.append(f"{transition}: {_spoken_text(focus)}. Use this as the anchor, then use the next signal to decide the priority.")
                continue
            narration_parts.append(
                f"{transition}: {_spoken_text(focus)}. {_english_focus_bridge(point)} {_business_meaning(point)} {_practical_action(point)}"
            )
            continue
        narration_parts.append(
            f"{_point_opener(ordinal, point)} {_business_meaning(point)} {_practical_action(point)}"
        )

    return clean_script_text(" ".join(narration_parts))


def _points_for_target(points: list[str], target_words: int) -> list[str]:
    if target_words <= 72:
        limit = 1
    elif target_words <= 130:
        limit = 2
    elif target_words <= 190:
        limit = 3
    elif target_words <= 270:
        limit = 4
    else:
        limit = 5
    return points[:limit]


def _point_opener(index: int, point: str) -> str:
    spoken_point = _spoken_text(point)
    return f"{_english_point_transition(index)}: {spoken_point}."


def _english_point_transition(index: int) -> str:
    return "First point" if index <= 1 else "Next point"


def _hinglish_point_transition(index: int) -> str:
    return "First point" if index <= 1 else "Next point"


def _english_focus_bridge(point: str) -> str:
    if _has_metric(point):
        return "That metric is the key signal here."
    return "This is the key item in this report."


def _hinglish_focus_bridge(point: str) -> str:
    if _has_metric(point):
        return "Ye metric yahan ka key signal hai."
    return "Ye report ka key item hai."


def _has_metric(text: str) -> bool:
    return bool(re.search(r"\d|%|\$|/100", text or ""))


def _business_meaning(point: str) -> str:
    lower = point.lower()
    if any(term in lower for term in ("knowledge graph", "entity card", "geo", "ai overview", "serp", "sitelink")):
        return "This matters because branded search and AI answers depend on clean entity signals, not just good-looking pages."
    if any(term in lower for term in ("schema", "structured data", "json-ld", "microdata")):
        return "This is a structure gap; without it, search engines and AI systems have less confidence in what the page represents."
    if any(term in lower for term in ("favicon", "open graph", "og:", "og title", "og description")):
        return "It looks small, but it shapes first impressions in browser tabs, previews, and shared links."
    if any(term in lower for term in ("internal link", "pagerank", "navigation")):
        return "This is about site architecture: when links are noisy, priority pages become harder for both users and search engines to read."
    if any(term in lower for term in ("seo", "search", "organic", "ranking", "keyword", "traffic")):
        return "This is a visibility signal. If this improves, the right people have a better chance of finding you before they shortlist someone else."
    if any(term in lower for term in ("conversion", "lead", "form", "enquiry", "demo", "contact")):
        return "This connects directly to lead flow, because even a small hesitation can reduce the number of qualified enquiries."
    if any(term in lower for term in ("speed", "technical", "error", "crawl", "index", "mobile", "loading")):
        return "This is friction. When technical friction stays unresolved, it quietly affects confidence, discovery, and momentum."
